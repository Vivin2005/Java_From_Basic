public class VariableDataTypes {
    public static void main(String[] args) {
        /*
          Data types are two types they are primitives and non-primitives.
          Primitives are,
              Integer -> It have the (byte,short,long). (1,2,3,4,5,6,....etc)
              Float -> Store the decimal values. Also (Double also available) (23.45,5.6,....,etc).
              Character -> could be a single char like ('a','A','i'..etc)
              Boolean -> are either (true/false)
         */
        /*
        ----Integer
            --> Integer have 4 bytes (32bits).
            --> Long have 8 bytes (64bits).
            --> Short have 2 bytes (16bits).
            --> Byte have 1 bytes (8bits).
        -- Int is the default value.
    -------------------------------------------------------------
        ----Float
            --> Float have 4 bytes (32bits).
            --> Double have 8 bytes (64bits).
        -- double is the default value.
    -------------------------------------------------------------
        ---- Character
            --> char have 2 bytes (16bits).
            --> Only Accept single quotes ('a'). for single value.
            --> We can use the UNICODE.
    -------------------------------------------------------------
        ---- Boolean
            --> Either (true or false).
            --> Mainly used for the condition.
    -------------------------------------------------------------
         */

         int num1 = 10;
         byte by = 127;
         short s1 = 5555;
         long l1 = 1000890L;

         float f1 = 5.6F;
         double d1 = 45.67D;

         char c1 = 'A';

         boolean b1 = true;
    }
}
