public class IfElse {
    public static void main(String[] args){
        // Conditional Statement.
        // If a condition is true then go this way. else the condition is not true go this way.
        // Basically the condition is based on (True or false).
        int x = 18;
        if(x>10 && x<=20){
            System.out.println("Hello");
        }
        else{
            System.out.println("Bye");
        }
        int a = 8;
        int b = 7;
        if(a>b){
            System.out.println("The value of the A is : "+a);
            System.out.println("Thank You.");
        }
        else{
            System.out.println("The value of the B is : "+b);
        }
    }
}
