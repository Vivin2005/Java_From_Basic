import java.util.Scanner;

public class Loops01 {
    public static void main(String[] args){
        // Loop are used to iterate something repeatedly untill the condition get's satisfied.
        // Loops in java are (While loop, do-while loop,for loop and for-each).
        Scanner scan = new Scanner(System.in);
        System.out.println("Enter a number : ");
        int n = scan.nextInt();
        for(int i=0;i<n;i++){
            System.out.println("Sorry");
        }
    }
}
